## Les etapes pour reussir sa reconversion

### 1. Testez votre interet

Inscrivez-vous a des cours ou des stages pour confirmer que la ceramique est bien faite pour vous. Pratiquer regulierement pendant plusieurs mois vous donnera une idee realiste du quotidien d un ceramiste.

### 2. Formez-vous serieusement

