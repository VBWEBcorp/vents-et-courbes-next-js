### 3. Preparez votre projet d installation

Reflechissez a votre modele economique :
- Allez-vous vendre uniquement vos creations ?
- Proposerez-vous des cours ou des stages ?
- Travaillerez-vous sur commande ou en serie ?
- Visez-vous les particuliers, les boutiques, ou les galeries ?

