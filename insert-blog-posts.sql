Le choix de la terre est fondamental en céramique. Chaque type de terre a ses caractéristiques propres et convient à des usages différents.

## Les grandes familles de terres

### La Faïence (950-1100°C)

**Caractéristiques :**
- Température de cuisson basse
- Poreuse après cuisson
- Blanche ou colorée
- Facile à travailler

**Idéale pour :**
- Carreaux décoratifs
- Sculptures
- Objets décoratifs
- Débutants

### Le Grès (1200-1300°C)

**Caractéristiques :**
- Température de cuisson moyenne à haute
- Imperméable après cuisson
- Résistant et durable
- Couleur variable (blanc, gris, chamotte)

**Idéale pour :**
- **Vaisselle utilitaire**
- Objets fonctionnels
- Pièces d''extérieur
- Tournage et modelage

### La Porcelaine (1280-1400°C)

**Caractéristiques :**
- Température de cuisson très haute
- Translucide
- Extrêmement blanche
- Délicate à travailler

**Idéale pour :**
- Pièces fines et élégantes
- Vaisselle haut de gamme
- Objets précieux

