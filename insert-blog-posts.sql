  '# Les 5 erreurs courantes en tournage débutant

Le tournage céramique est une technique fascinante mais exigeante. Voici les erreurs les plus courantes et comment les éviter.

## 1. Une terre trop molle ou trop dure

La consistance de la terre est cruciale. **Une terre trop molle** s''affaissera sous son propre poids, tandis qu''**une terre trop dure** sera difficile à centrer et à monter.

### Solution :
- Pétrissez votre terre jusqu''à obtenir une consistance homogène
- Elle doit être ferme mais malléable
- Pensez à la consistance d''une pâte à modeler de qualité

## 2. Un centrage approximatif

Beaucoup de débutants se précipitent sur le montage sans avoir parfaitement centré leur boule de terre.

### Conséquences :
- Pièces déséquilibrées
- Épaisseurs irrégulières
- Risque d''effondrement

## 3. Trop d''eau

L''excès d''eau est l''ennemi du tournage. Elle ramollit la terre et fragilise vos pièces.

**Astuce** : Utilisez l''eau avec parcimonie et essuyez régulièrement l''excès avec une éponge.

## 4. Des gestes trop brusques

Le tournage demande de la douceur et de la progressivité.

### À retenir :
- Mouvements lents et réguliers
- Pression progressive
- Respiration calme

## 5. Négliger le tournassage

Le tournassage est aussi important que le tournage lui-même. C''est à cette étape que votre pièce prend sa forme définitive et son esthétique finale.

