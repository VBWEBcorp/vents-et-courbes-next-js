## Les differents etats de la terre en ceramique : guide pour bien les reconnaitre

En ceramique, la terre est une matiere vivante qui evolue en permanence. Ici nous parlerons du gres, sa texture et ses proprietes changent selon son taux d humidite, et chaque etape correspond a des gestes techniques specifiques.

## 1. Terre molle : l etat plastique

**Taux d humidite :** maximum

**Texture :** souple, elastique et tres malleable

**Utilisations :** tournage, modelage, faconnage a la plaque

**Particularite :** elle se deforme facilement et conserve la trace des doigts.

## 2. Terre ferme ou "durete cuir" : le moment cle

**Taux d humidite :** intermediaire

**Texture :** ferme mais encore legerement souple

**Utilisations :** tournassage et affinage des parois, assemblage d elements (anse, bec,...)

**Pourquoi cet etat est crucial :** la terre est assez resistante pour etre manipulee sans s affaisser, tout en permettant des collages solides grace a la barbotine (melange d eau et de terre).

## 3. Terre seche ou "os"

**Taux d humidite :** nul

**Texture :** dure, extremement fragile et cassante

**Objectif :** preparer la piece pour la premiere cuisson (dite biscuit).

**Attention :** la moindre trace d humidite peut entrainer des fissures ou un eclatement au four a cause de la vapeur d eau.

## 4. Biscuit : la premiere cuisson

**Temperature :** environ 950C

**Caracteristiques :**
- la piece devient solide
- elle reste poreuse pour absorber l email
- elle peut etre poncee pour corriger de petits defauts

**Utilisation :** etape indispensable avant l emaillage.

## 5. Email : la deuxieme cuisson

**Temperature :** generalement entre 1 220C et 1 280C (selon la terre utilisee)

**Resultat :** la terre se vitrifie, devenant etanche et tres resistante. C est le stade ou la piece est prete a l usage, qu il s agisse de vaisselle, de theieres ou d objets decoratifs.

