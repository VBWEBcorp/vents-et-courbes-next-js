## Le modelage : une porte d entree naturelle en ceramique

Beaucoup pensent que la ceramique commence forcement au tour. En realite, le modelage est souvent la voie la plus directe : on apprend a sentir la terre, a la comprimer, a la soutenir, a la lisser.
C est aussi une approche tres creative, qui permet de travailler des formes libres et d avancer a son rythme.

## Les 3 techniques de base a connaitre

### 1. Le pince (pinch)

C est la methode la plus simple et la plus instructive. On part d une boule de terre, puis on ouvre et on monte les parois en pincant progressivement.
Le pince apprend l epaisseur, la regularite et la patience. Un bol bien pince est souvent plus solide qu on ne l imagine, car la terre est compressee au fur et a mesure.

### 2. Le colombin

Le colombin consiste a monter une piece en superposant des boudins de terre.
On construit la forme en spirale ou par etages, puis on soude les jonctions en lissant l interieur et/ou l exterieur. Cette technique permet des volumes plus grands, et des lignes tres organiques.

### 3. La plaque

On etale la terre au rouleau pour obtenir une plaque d epaisseur reguliere, puis on decoupe et on assemble (boites, assiettes, tasses, pieces architecturales).
La plaque demande une bonne gestion du sechage, car les elements doivent etre assembles au bon moment, a la meme durete.

## L assemblage : le vrai point cle du modelage

Une piece modelee est souvent composee de plusieurs elements : parois, fond, anse, couvercle, decor en relief...
Pour assembler correctement :
- Les elements doivent etre au meme etat (souvent au stade cuir)
- On griffe les deux zones a assembler
- On utilise de la barbotine
- On presse et on lisse le joint, sans exces d eau

Un assemblage bien fait devient presque invisible. Un assemblage trop humide ou trop sec finit souvent par fissurer.

## Les finitions qui changent tout

Le modelage permet de travailler la surface avec beaucoup de finesse :
- Lissage a la main ou a la esteque
- Textures (tampons, tissus, outils, gravure)
- Decoupe et ajours
- Engobes et decors sur terre crue

Ces finitions sont plus faciles a reussir quand la piece n est plus trop molle : le bon moment se situe souvent entre la fin du faconnage et le debut du raffermissement.

## Sechage : la meme exigence que pour le tournage

Meme sans tour, le sechage reste determinant. Les pieces modelees ont souvent des epaisseurs variables, donc elles demandent encore plus de vigilance.
On privilegie un sechage lent, protege, et on surveille particulierement les zones ajoutees (anses, collages, reliefs).

## Les erreurs courantes a eviter

Certaines erreurs reviennent souvent :
- Ajouter trop d eau (la terre se fragilise)
- Assembler au mauvais moment
- Negliger la compression (parois qui fissurent)
- Vouloir aller vite sur le sechage

Le modelage recompense la lenteur et l attention. La terre se laisse faire, a condition de respecter son rythme.

## En resume

Le modelage est une pratique complete, accessible et tres formatrice. Il permet de comprendre la structure d une piece, l epaisseur, l assemblage et le sechage, tout en developpant une vraie liberte de formes.

