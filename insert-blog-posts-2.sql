Tout commence au tour de potier. La terre est centree, puis montee en forme pour creer le corps de la theiere. On privilegie une forme stable, legerement arrondie, qui retiendra bien la chaleur. L epaisseur doit etre reguliere pour eviter les tensions lors des cuissons.

